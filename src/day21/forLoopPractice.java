package day21;

public class forLoopPractice {

	public static void main(String[] args) {
		
		
		for (int i = 1; i <= 5; i++) {
			
			System.out.print(i + " ");
		}
		System.out.println();
		
		int j ;
		for (j = 1; j <= 10 ; j++) {
			
			System.out.print(j + " ");
		}
		
		System.out.println();
		
        for (int i = 1; i <= 10; i += 2) {
			
			System.out.print(i + " ");
		}

	}

}
