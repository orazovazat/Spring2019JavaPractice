package day5;

public class shortHandOperator {

	public static void main(String[] args) {
	

		int i = 10 ;
		System.out.println("the score is " + i);

		// i = i + 5 ;  // i = 15 ;
		
		i += 5 ;
		
		System.out.println("the score is " + i);
		
		// i = i - 2 ; // i = 13 ;
		
		i -= 2 ; 
		
		System.out.println("the score is " + i);
		
		// i = i * 10 ; // i = 130 ;
		
		i *= 10 ;
		
		System.out.println("the score is " + i);
		
		// i = i / 5 ; // i = 26 ;
		
		i /= 5 ; 
		
		System.out.println("the score is " + i);
		
		// i = i % 7 ; // i = 5 ;
		
		i %= 7 ;
		
		System.out.println("the score is " + i);
		
		
	}

}
